// push  
var fruits=["Banana", "Orange", "Apple", "Mango"]
fruits.push("kiwi");
console.log(fruits);

// pop
var fruits=["Banana", "Orange", "Apple", "Mango"]
console.log(fruits.pop());

// delete
var fruits=["Banana", "Orange", "Apple", "Mango"];
delete fruits[0];
console.log(fruits);

// shift
var fruits=["Banana", "Orange", "Apple", "Mango"];
fruits.shift();
console.log(fruits);

// .tostring
var fruits=["Banana", "Orange", "Apple", "Mango"]
console.log(fruits.toString());

// unshift
var fruits=["Banana", "Orange", "Apple", "Mango"];
fruits.unshift("Lemon");
console.log(fruits);


